class Sete {
	public static void main (String [] args) {
		for (int num = 3; num <= 48; num += 5) System.out.print(num+ ", ");
		System.out.println ();
	
		for (int num = -2; num <= 43; num += 5) System.out.print(num+ ", ");
		System.out.println ();
		
		for (int num = 48; num >= 3; num -= 5) System.out.print(num+ ", ");
		System.out.println ();		
	}
}