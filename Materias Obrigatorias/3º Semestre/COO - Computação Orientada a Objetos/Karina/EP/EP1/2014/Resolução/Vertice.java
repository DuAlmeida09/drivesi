class Vertice{		//clsse Vertice
	int valor;		//Valor ou indice do vertice
	int coordx;		//coordenada x do vertice
	int coordy;		//coordenada y do vertice

	Vertice(int valor, int x, int y){	//construtor, recebe todos os dados e transfere para as variaveis globais da classe
		this.valor = valor;
		coordy = y;
		coordx = x;
	}
}